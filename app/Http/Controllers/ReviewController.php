     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data = DB::table('review as r')
        ->join('users as u', 'r.users_id', '=', 'u.id')
        ->select('r.*', 'u.name as name')
        ->get();
        return view('admin.review.index', compact('data'));
